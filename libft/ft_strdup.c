#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;

	if (!(dest = (char *)malloc(sizeof(*s) * (ft_strlen(s) + 1))))
		return (NULL);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = s[i];
	return (dest);
}
